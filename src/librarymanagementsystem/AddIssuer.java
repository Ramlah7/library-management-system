    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        // TODO add your handling code here:
        String full_name=full_name_txtfield.getText();
        String email=answertxt.getText();
        String username=usernametxtfield.getText();
        String password=passwordtxt.getText();
        String confirmPswrd=confirmpaswords.getText();
        String security_question=securityquestion.getText();
        String security_answer=answertxt.getText();
        if(password.equals(confirmPswrd)){
            try {
                ConnectionClass obj=new ConnectionClass();
                String query="insert into issuer(username,full_name,email,password,security_question,security_answer)values('"+username+"','"+full_name+"','"+email+"','"+password+"','"+security_question+"','"+security_answer+"')";

                int rowschanged=obj.stmt.executeUpdate(query);
                if(rowschanged>0){
                    message.setText("issuer added successfully.");
                } else {
                    message.setText("error occured during adding issuer");
                }
            }
            catch (Exception e) {
                message.setText("An error occurred while adding the issuer.");
                e.printStackTrace();
            }
        }
        else{
            message.setText("password and confirm password must be same");
        }
    }//GEN-LAST:event_signUpActionPerformed

